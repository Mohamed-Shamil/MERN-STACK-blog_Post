import { toast } from "react-toastify";

const toastifyNotifications = () => {
  const invalidRegToast = () => {
    toast.error("Invalid Form !", {
      position: "top-right",
      autoClose: 3000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
      theme: "dark",
    });
  };

  const invalidCredToast = () => {
    toast.error("Invalid Email !", {
      position: "top-left",
      autoClose: 5000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
      theme: "dark",
    });
  };

  const invalidLogin = () => {
    toast.error("email or password in correct", {
      position: "top-right",
      autoClose: 1000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
      theme: "dark",
    });
  };

  const RegSuccessToast = () => {
    toast.success("successfully Registered ", {
      position: "top-center",
      autoClose: 3000,
      hideProgressBar: false,
      closeOnClick: true,
      pauseOnHover: true,
      draggable: true,
      progress: undefined,
      theme: "dark",
    });
  };
  return { RegSuccessToast, invalidCredToast, invalidLogin, invalidRegToast };
};

export default toastifyNotifications;
